    private void Btn_Abrir1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_Abrir1ActionPerformed
            
        NivelPrioridadeDAO nv = new NivelPrioridadeDAO();
        ResultSet rs = nv.listaNivel();

        DefaultTableModel model  = (DefaultTableModel) Tb_Niveis.getModel();
        model.setRowCount(0);
        try {
            while(rs.next()) {
                model.addRow(new String[]{ rs.getString("ID"), rs.getString("DESCRICAO")});
            }
        } catch (SQLException e) {
            System.out.println("Erro: "+e.getMessage());
        }
    }//GEN-LAST:event_Btn_Abrir1ActionPerformed

    private void Btn_CadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_CadastroActionPerformed
       
        TicketNivelPrioridade tn = new TicketNivelPrioridade();
        NivelPrioridadeDAO nv = new NivelPrioridadeDAO();
            
        if(Txt_Descricao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Favor preecha todos os campos!");
        } else {
            
            tn.setDescricao(Txt_Descricao.getText());

            nv.cadastroNivel(tn);
            Txt_Descricao.setText("");
            
        }
    }//GEN-LAST:event_Btn_CadastroActionPerformed

    private void Btn_AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_AlterarActionPerformed
        TicketNivelPrioridade tn = new TicketNivelPrioridade();
        NivelPrioridadeDAO nv = new NivelPrioridadeDAO();
            
        if(Txt_Descricao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Favor selecione e carregue um nivel de prioridade em tela!");
        } else {
            String li = String.valueOf(Tb_Niveis.getValueAt(Tb_Niveis.getSelectedRow(), Tb_Niveis.getSelectedColumn()));
        
            int res = JOptionPane.showConfirmDialog(null, "Deseja alterar a descrição do nivel de prioridade?", "Alterar nivel", JOptionPane.YES_NO_OPTION);
            if (res == JOptionPane.YES_OPTION) {

                tn.setId(Integer.parseInt(li));
                tn.setDescricao(Txt_Descricao.getText());

                nv.alteraNivel(tn);
                Txt_Descricao.setText("");
            } 
        } 
   
    }//GEN-LAST:event_Btn_AlterarActionPerformed

    private void Btn_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_ExcluirActionPerformed
         TicketNivelPrioridade tn = new TicketNivelPrioridade();
        NivelPrioridadeDAO nv = new NivelPrioridadeDAO();
            
        if(Txt_Descricao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Favor selecione e carregue um nivel de prioridade em tela!");
        } else {
            String li = String.valueOf(Tb_Niveis.getValueAt(Tb_Niveis.getSelectedRow(), Tb_Niveis.getSelectedColumn()));
        
            int res = JOptionPane.showConfirmDialog(null, "Deseja excluir o nivel de prioridade?", "Excluir nivel", JOptionPane.YES_NO_OPTION);
            if (res == JOptionPane.YES_OPTION) {

                tn.setId(Integer.parseInt(li));
                nv.deletaNivel(tn);
                Txt_Descricao.setText("");
            } 
        } 
    }//GEN-LAST:event_Btn_ExcluirActionPerformed

