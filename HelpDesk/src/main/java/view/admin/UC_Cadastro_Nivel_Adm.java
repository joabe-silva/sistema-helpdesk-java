    private void Btn_Abrir2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_Abrir2ActionPerformed
        TicketNivelPrioridade tn = new TicketNivelPrioridade();
        NivelPrioridadeDAO nv = new NivelPrioridadeDAO();
        
        String li = String.valueOf(Tb_Niveis.getValueAt(Tb_Niveis.getSelectedRow(), Tb_Niveis.getSelectedColumn()));
        tn.setId(Integer.parseInt(li));
        
        try {
            ResultSet rs = nv.nivel(tn);
            if(rs.next()) {
                Txt_Descricao.setText(rs.getString("DESCRICAO")); 
            }    
        } catch (SQLException e) {
            System.out.println("Erro ao listar usuario: "+e.getMessage());
        }
    }//GEN-LAST:event_Btn_Abrir2ActionPerformed


